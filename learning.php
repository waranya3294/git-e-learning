    <div class="container">
        <div class="card shadow-sm rounded-1 mb-3" style="border: none;border-top: 4px solid #00adb0;">
            <div class="card-body">

                <h4>1.1 ประเภทของการพ่นสี</h4>
                <h5 class="mt-4">ประเภทของการพ่นสี (Types of Paint Spraying)</h5>
                <p class="text-muted">
                    การพ่นสีเป็นกระบวนการที่นิยมใช้ในงานตกแต่งพื้นผิวและอุตสาหกรรมหลายประเภท เนื่องจากให้ผลลัพธ์ที่เรียบเนียน สม่ำเสมอ และสามารถครอบคลุมพื้นที่ขนาดใหญ่ได้ในเวลาอันสั้น ประเภทของการพ่นสีสามารถแบ่งได้ตามลักษณะของเครื่องมือและเทคนิคที่ใช้ ดังนี้:
                </p>

                <!-- การพ่นสีด้วยมือ -->
                <div class="mb-4">
                    <h5>1. การพ่นสีด้วยมือ (Manual Spraying)</h5>
                    <p>
                        เป็นการพ่นสีที่ใช้แรงงานคนในการควบคุมเครื่องพ่นสี เหมาะสำหรับงานที่ต้องการความละเอียดและการควบคุมเฉพาะจุด เช่น:
                    </p>
                    <ul>
                        <li>งานตกแต่งภายในบ้าน</li>
                        <li>การพ่นสีรถยนต์ในบางส่วน</li>
                        <li>งานศิลปะและงานประดิษฐ์</li>
                    </ul>
                    <p><strong>ข้อดี:</strong> ควบคุมทิศทางและความหนาของสีได้ง่าย เหมาะสำหรับงานขนาดเล็กหรือพื้นที่เข้าถึงยาก</p>
                    <p><strong>ข้อเสีย:</strong> ใช้เวลามาก และอาจเกิดข้อผิดพลาดจากมนุษย์ เช่น ความไม่สม่ำเสมอของสี</p>
                </div>

                <!-- การพ่นสีด้วยระบบแรงดันอากาศ -->
                <div class="mb-4">
                    <h5>2. การพ่นสีด้วยระบบแรงดันอากาศ (Air Spray)</h5>
                    <p>
                        ใช้แรงดันอากาศจากเครื่องอัดลม (Compressor) ในการดันสีออกจากหัวพ่น สีจะถูกพ่นออกมาในรูปของละอองละเอียด เหมาะสำหรับ:
                    </p>
                    <ul>
                        <li>งานพ่นสีรถยนต์</li>
                        <li>งานอุตสาหกรรมเฟอร์นิเจอร์</li>
                        <li>งานตกแต่งที่ต้องการพื้นผิวเรียบเนียน</li>
                    </ul>
                    <p><strong>ข้อดี:</strong> ให้ผลลัพธ์ที่เรียบและเนียน ปรับปริมาณและความแรงของการพ่นได้ง่าย</p>
                    <p><strong>ข้อเสีย:</strong> สิ้นเปลืองสีเนื่องจากละอองบางส่วนกระจายไปในอากาศ และต้องใช้อุปกรณ์เพิ่มเติม เช่น เครื่องอัดลม</p>
                </div>

                <!-- การพ่นสีด้วยระบบไร้อากาศ -->
                <div class="mb-4">
                    <h5>3. การพ่นสีด้วยระบบไร้อากาศ (Airless Spray)</h5>
                    <p>
                        ใช้ปั๊มแรงดันสูงเพื่อดันสีออกจากหัวพ่นโดยไม่ใช้อากาศ สีจะถูกพ่นออกมาในลักษณะของสเปรย์ที่ละเอียด เหมาะสำหรับ:
                    </p>
                    <ul>
                        <li>งานพ่นสีผนังอาคาร</li>
                        <li>งานพ่นสีโครงสร้างเหล็ก</li>
                        <li>งานในอุตสาหกรรมหนัก เช่น เรือและเครื่องจักร</li>
                    </ul>
                    <p><strong>ข้อดี:</strong> ลดการกระจายตัวของสีในอากาศ พ่นสีได้ปริมาณมากและครอบคลุมพื้นที่กว้าง</p>
                    <p><strong>ข้อเสีย:</strong> ต้องการแรงดันสูง อาจเกิดปัญหาอุปกรณ์เสียหายได้ง่าย ต้องใช้ความระมัดระวังในระหว่างการใช้งาน</p>
                </div>

                <!-- การพ่นสีด้วยระบบไฟฟ้าสถิต -->
                <div class="mb-4">
                    <h5>4. การพ่นสีด้วยระบบไฟฟ้าสถิต (Electrostatic Spray)</h5>
                    <p>
                        ใช้หลักการของไฟฟ้าสถิต โดยการชาร์จอนุภาคสีด้วยประจุไฟฟ้าขั้วหนึ่ง และให้พื้นผิวงานมีประจุตรงข้าม สีจะถูกดูดเข้าหาพื้นผิวอย่างมีประสิทธิภาพ เหมาะสำหรับ:
                    </p>
                    <ul>
                        <li>การพ่นสีโลหะ</li>
                        <li>งานในอุตสาหกรรมรถยนต์และอิเล็กทรอนิกส์</li>
                    </ul>
                    <p><strong>ข้อดี:</strong> ลดการสูญเสียสี เนื่องจากอนุภาคสีถูกดูดเข้าหาพื้นผิว ให้การเคลือบที่เรียบและสม่ำเสมอ</p>
                    <p><strong>ข้อเสีย:</strong> ต้องการอุปกรณ์ที่มีเทคโนโลยีสูง ใช้งานยากกับวัสดุที่ไม่นำไฟฟ้า</p>
                </div>

                <!-- การพ่นสีด้วยระบบ HVLP -->
                <div class="mb-4">
                    <h5>5. การพ่นสีด้วยระบบ HVLP (High Volume Low Pressure)</h5>
                    <p>
                        ใช้ปริมาณอากาศสูงแต่ความดันต่ำในการพ่นสี เหมาะสำหรับงานที่ต้องการลดการกระจายของสีในอากาศ เช่น:
                    </p>
                    <ul>
                        <li>งานตกแต่งภายใน</li>
                        <li>งานพ่นสีเฟอร์นิเจอร์</li>
                    </ul>
                    <p><strong>ข้อดี:</strong> ลดการสิ้นเปลืองสี และลดการกระจายตัวของละอองสีในอากาศ</p>
                    <p><strong>ข้อเสีย:</strong> ใช้เวลาพ่นนานกว่าระบบแรงดันสูง และไม่เหมาะสำหรับงานพื้นที่กว้าง</p>
                </div>
